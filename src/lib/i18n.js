export function useT(uiLang) {
  const dict = STRINGS[uiLang] || STRINGS.it
  return (key) => dict[key] ?? STRINGS.it[key] ?? key
}

/** Pick bilingual content based on UI language */
export function pick(bilingual, uiLang = 'it') {
  if (!bilingual) return ''
  if (typeof bilingual === 'string') return bilingual
  return bilingual[uiLang] ?? bilingual.it ?? bilingual.en ?? ''
}
